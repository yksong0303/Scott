package s0619;

public class ArrayTest5 {
	public static void main(String[] args) {
		for (int i = 10; i >= 1; i--) {
			for (int j = i; j >= 1; j--) {

				System.out.print(j + ",");
			}
			System.out.println();

		}
		
		for (int i = 1; i <= 7; i++) {
			for (int j = i; j <= 7; j++) {

				System.out.print(j + ",");
			}
			System.out.println();

		}
		
		for (int i = 1; i <= 4; i++) {
			for (int j = i; j <= 4; j++) {

				System.out.print(j + ",");
			}
			System.out.println();

		}
		

	}
}
